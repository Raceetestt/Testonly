def solid_line(char='-', width=None, color='white'):
    # ANSI escape codes for colors
    colors = {
        'black': '\033[30m',
        'red': '\033[31m',
        'green': '\033[32m',
        'yellow': '\033[33m',
        'blue': '\033[34m',
        'magenta': '\033[35m',
        'cyan': '\033[36m',
        'white': '\033[37m',
        'reset': '\033[0m'  # Reset to default color
    }

    # Get the terminal width
    if width is None:
        try:
            width = os.get_terminal_size().columns
        except OSError:
            width = 80  # Default width if terminal size cannot be determined

    # Get the color code
    color_code = colors.get(color, colors['white'])  # Default to white if color not found

    # Print the solid line in the specified color
    print(f"{color_code}{char * width}{colors['reset']}")

